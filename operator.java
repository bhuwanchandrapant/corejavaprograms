public class operator {
    public static void main(String[] args) {
        
        int a = 6;
        int b = 4;
        b += 3;
        int s = a % b;  //modulo operator
         System.out.println(s);
        System.out.println(b);
        System.out.println(23==24); // comparison operator
        System.out.println(21>20 && 21>12); //logical operator
        System.out.println();
        
    }
}
